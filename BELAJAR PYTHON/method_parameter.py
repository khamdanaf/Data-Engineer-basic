# Belajar method parameter

# satu parameter
def say_hello(nama):
    print(f"Assalamualaikum {nama}")
say_hello('Emir')
say_hello('Khamdan')
say_hello('Yumna')

#beberapa para meter
def say_hello(first_name, last_name):
    print(f"Assalamualaikum {first_name} {last_name}")
say_hello('fakhry' , 'Emirkhan')
say_hello('Khamdan','Annas')
say_hello('Efa','Yumna')